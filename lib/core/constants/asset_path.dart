class AssetPath {
  static String getVector(String fileName) {
    return 'assets/vectors/$fileName';
  }

  static String getRive(String fileName) {
    return 'assets/rive_assets/$fileName';
  }

  static String getImage(String fileName) {
    return 'assets/images/$fileName';
  }

  static String getIcons(String fileName) {
    return 'assets/icons/$fileName';
  }
}
